"""
14) List Remove Duplicates
Question
Write a program (function!) that takes a list and returns a new list that contains all the elements of the first list minus all the duplicates.
Solution
"""
def liste(x):
   numbers = set(x)
   new = list(numbers)
   return print(new)
y = [1,2,3,4,5,5,6,7,7,9]
liste(y)
